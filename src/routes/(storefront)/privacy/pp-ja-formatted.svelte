<h1>プライバシーポリシー</h1>
<p>
    JWP
    Consulting合同会社（以下「当社」といいます。）は、当社サイト及び当社サービス（以下併せて「当社サイト等」といいます。）における個人情報等の取扱いについて、当社サイト等のご利用者（以下単に「ご利用者」といいます。）のプライバシー保護に努めています。当社サイト等をご利用される前に、このプライバシーポリシー（以下「本プライバシーポリシー」といいます。）を最後までよくお読み下さいますようお願い致します。
</p>
<h2>第１条（定義）</h2>
<p>本プライバシーポリシーにおける用語の定義は、以下のとおりです。</p>
<ol>
    <li>
        <p>
            「個人情報」とは、個人情報保護法上の個人情報、すなわち、生存する個人に関する情報であって、当該情報に含まれる氏名、生年月日、住所、電話番号、連絡先その他の記述等により特定の個人を識別できる情報又は個人識別符号が含まれるものをいいます。
        </p>
    </li>
    <li>
        <p>
            「個人情報データベース等」とは、個人情報保護法上の個人情報データベース等、すなわち、個人情報を含む情報の集合物であって、特定の個人情報を電子計算機を用いて検索することができるように体系的に構成したもの、その他政令で定めるものをいいます。
        </p>
    </li>
    <li>
        <p>
            「個人データ」とは、個人情報保護法上の個人データ、すなわち、個人情報データベース等を構成する個人情報をいいます。
        </p>
    </li>
</ol>
<h2>第２条（本プライバシーポリシーの適用範囲）</h2>
<ol>
    <li>
        <p>
            本プライバシーポリシーは、当社サイト等による個人情報等の取扱いに関して、当社とご利用者との間で適用されるものとします。
        </p>
    </li>
    <li>
        <p>
            EU一般データ保護規則（GDPR）及びこれに関連する各国施行法が適用される場合、本プライバシーポリシーに加えて、当社GDPRプライバシーポリシー（以下「GDPRプライバシーポリシー」といいます。）も適用されるものとします。
        </p>
    </li>
    <li>
        <p>
            本プライバシーポリシーの内容とGDPRプライバシーポリシーの内容に相違がある場合、GDPRプライバシーポリシーの内容が優先的に適用されるものとします。
        </p>
    </li>
</ol>
<h2>第３条（個人情報の収集方法）</h2>
<p>
    当社は、ご利用者が当社サイト等を利用する際に、個人情報のご提供を頂く、又は個人情報を収集することがあります。また、当社は、当社と提携する第三者から個人情報を取得することがあります。
</p>
